//  Дана переменная sum (сумма покупки).
//  В зависимости от значения переменной sum рассчитать персональную скидку и
//  вывести в консоль сумму к оплате с учетом скидки!:
//     сумма до 1000 - скидка не предусмотрена
//     сумма от 1000 до 2000 - скидка 5%
//     сумма от 2000 до 3000 - скидка 10%
//     сумма от 3000 - скидка 10% + подарок

let sum =2756;
let discountFive = 0.05;
let discountTen = 0.1;
let answer;

if (sum < 1000) {
  answer = "скидка не предусмотрена";
} else if (sum >= 1000 && sum < 2000) {
  sum = sum - (sum * discountFive);
  answer = "ваша скидка 5%";
} else if (sum >= 2000 && sum < 3000) {
  sum = sum - (sum * discountTen);
  answer = "ваша скидка 10%";
} else if (sum >= 3000) {
  sum = sum - (sum * discountTen);
  answer = "ваша скидка 10% + подарок";
} else {
  answer = "что-то пошло не так";
}
console.log(`Сумма вашей покупки составит: ${sum},`, answer);

