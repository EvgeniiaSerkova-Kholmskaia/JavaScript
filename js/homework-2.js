// Дана константа month (название месяца), в зависимости от названия месяца
// присвоить значение переменной season:
// декабрь / январь / февраль  -> присвоить значение "зима"
// март / апрель / май  -> присвоить значение "весна"
// июнь / июль / август  -> присвоить значение "лето"
// сентябрь / окрябрь / ноябрь  -> присвоить значение "осень"

let month = "август";
let season;
switch (month) {
  case "декабрь":
  case "январь":
  case "февраль":
    season = "зима";
    break;
  case "март":
  case "апрель":
  case "май":
    season = "весна";
    break;
  case "июнь":
  case "июль":
  case "август":
    season = "лето";
    break;
  case "сентябрь":
  case "октябрь":
  case "ноябрь":
    season = "осень";
    break;
  default:
    season = "неправильно написан месяц";
}
console.log(`Текущий сезон: ${season}`);
